package models;

/**
 *
 * @author UNICARTAGENA
 */
public class Animal {
    private String especie;
    private String nombre;
    private int edad;
    private String genero;

    public Animal() {
    }

    public Animal(String especie, String nombre, int edad, String genero) {
        this.especie = especie;
        this.nombre = nombre;
        this.edad = edad;
        this.genero = genero;
    }

    public String getEspecie() {
        return especie;
    }

    public void setEspecie(String especie) {
        this.especie = especie;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }
    
    
    
}
